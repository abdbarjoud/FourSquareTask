import UIKit

/*
 Set of commmont theme colors used in application
*/
extension UIColor {
    static func fSOrange() -> UIColor
    {
        return UIColor(red:237.0/255.0, green:48.0/255.0, blue:42.0/255.0, alpha: 1)
    }
    
    static func fSGreen() -> UIColor
    {
        return UIColor(red:100.0/255.0, green:201.0/255.0, blue:51.0/255.0, alpha: 1)
    }
    
    static func fSLightGray() -> UIColor
    {
        return UIColor(red:165.0/255.0, green:165.0/255.0, blue:165.0/255.0, alpha: 1)
    }
    
    static func fSDarkGray() -> UIColor
    {
        return UIColor(red:62.0/255.0, green:71.0/255.0, blue:75.0/255.0, alpha: 1)
    }
    
    
    
}